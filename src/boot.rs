/// `doctrine boot install` — resolve harnesses, union + dedup their import
/// targets, prepend the `@`-import **once**, then refresh each harness, isolating
/// a single harness's failure (it is printed; the others still run — A9/§5.5).
pub(crate) fn run_install(
    path: Option<PathBuf>,
    agents: &[String],
    dry_run: bool,
    yes: bool,
) -> anyhow::Result<()> {
    let root = root::find(path, &root::default_markers())?;
    let exec = resolve_exec()?;
    let harnesses = resolve_harnesses(agents, &root)?;

    if !yes && !dry_run {
        let label: Vec<&str> = harnesses.iter().map(harness_label).collect();
        let proceed = install::prompt_confirm(&format!(
            "Wire doctrine boot ({}) into {}? [y/N] ",
            label.join(", "),
            root.display()
        ))?;
        if !proceed {
            writeln!(io::stdout(), "Aborted.")?;
            return Ok(());
        }
    }
    wire(&root, &exec, &harnesses, dry_run)
}

/// The exec-injected orchestration core: the shell (`run_install`) resolves
/// `current_exe()` and prompts; this does the work — union + dedup import
/// targets, prepend the `@`-import **once**, then refresh each harness, isolating
/// a single harness's failure (it is printed; the others still run — A9/§5.5).
pub(crate) fn wire(
    root: &Path,
    exec: &Path,
    harnesses: &[Harness],
    dry_run: bool,
) -> anyhow::Result<()> {
    let reference = format!("@{BOOT_REL}");
    let targets: Vec<PathBuf> = harnesses
        .iter()
        .flat_map(|h| import_targets(h, root))
        .collect();

    let mut stdout = io::stdout();
    let tag = if dry_run { "[dry-run] " } else { "" };

    for outcome in ensure_boot_import(&targets, &reference, dry_run)? {
        let (verb, target) = match &outcome {
            RefOutcome::Created(p) => ("created", p),
            RefOutcome::Added(p) => ("added ref", p),
            RefOutcome::Present(p) => ("ref present", p),
        };
        writeln!(stdout, "  {tag}{verb}: {}", target.display())?;
    }

    for h in harnesses {
        match install_refresh(h, root, exec, dry_run) {
            Ok(report) => {
                match report.hook {
                    RefreshOutcome::Wired(cmd) => {
                        writeln!(stdout, "  {tag}{}: wired hook: {cmd}", harness_label(h))?;
                        if matches!(h, Harness::Codex) {
                            writeln!(
                                stdout,
                                "  {tag}{}: wrote {CODE_HOOKS_REL}. To activate:",
                                harness_label(h)
                            )?;
                            writeln!(
                                stdout,
                                "    1. Ensure [features] hooks = true in .codex/config.toml."
                            )?;
                            writeln!(
                                stdout,
                                "    2. Start codex in this project and accept the project trust prompt."
                            )?;
                            writeln!(
                                stdout,
                                "    3. Run /hooks in codex to trust the doctrine hook."
                            )?;
                        }
                    }
                    RefreshOutcome::Refreshed(cmd) => {
                        writeln!(stdout, "  {tag}{}: refreshed hook: {cmd}", harness_label(h))?;
                        if matches!(h, Harness::Codex) {
                            writeln!(
                                stdout,
                                "  {tag}{}: wrote {CODE_HOOKS_REL}. To activate:",
                                harness_label(h)
                            )?;
                            writeln!(
                                stdout,
                                "    1. Ensure [features] hooks = true in .codex/config.toml."
                            )?;
                            writeln!(
                                stdout,
                                "    2. Start codex in this project and accept the project trust prompt."
                            )?;
                            writeln!(
                                stdout,
                                "    3. Run /hooks in codex to trust the doctrine hook."
                            )?;
                        }
                    }
                    RefreshOutcome::PrintedFallback { hook_file, snippet } => {
                        writeln!(
                            stdout,
                            "  {}: {hook_file} is malformed — add this hook manually:",
                            harness_label(h)
                        )?;
                        writeln!(stdout, "{snippet}")?;
                    }
                    RefreshOutcome::None => {}
                }
                if report.spike_warning {
                    writeln!(
                        stdout,
                        "  {}: warning — a foreign SessionStart hook with the same matcher",
                        harness_label(h)
                    )?;
                    writeln!(
                        stdout,
                        "    ({SESSION_MATCHER_CODEX}) exists alongside the doctrine hook."
                    )?;
                    writeln!(
                        stdout,
                        "    Both will fire concurrently. Remove the old entry to avoid"
                    )?;
                    writeln!(stdout, "    duplicate snapshot injection.")?;
                }
                // baseRef leg (SL-064 §8): report a fresh set or a respected
                // non-head override. AlreadyHead / PrintedFallback / NotApplicable
                // stay silent (no-op, or the hook leg already flagged a bad file).
                match report.baseref {
                    BaseRefOutcome::Set => {
                        writeln!(
                            stdout,
                            "  {tag}{}: set worktree.baseRef=head",
                            harness_label(h)
                        )?;
                    }
                    BaseRefOutcome::Conflict(value) => {
                        writeln!(
                            stdout,
                            "  {}: worktree.baseRef is '{value}', not 'head' — left as-is (claude isolated-worker base==B needs 'head'; set it manually if intended)",
                            harness_label(h)
                        )?;
                    }
                    BaseRefOutcome::AlreadyHead
                    | BaseRefOutcome::PrintedFallback
                    | BaseRefOutcome::NotApplicable => {}
                }
                // MCP registration leg (CHR-013): report a fresh wire or a stale
                // refresh; a foreign/malformed `.mcp.json` entry prints the manual
                // snippet. None (current, or pi) stays silent.
                match report.mcp {
                    RefreshOutcome::Wired(cmd) => {
                        writeln!(
                            stdout,
                            "  {tag}{}: registered MCP server in {MCP_REL}: {cmd} serve --mcp",
                            harness_label(h)
                        )?;
                    }
                    RefreshOutcome::Refreshed(cmd) => {
                        writeln!(
                            stdout,
                            "  {tag}{}: refreshed MCP server in {MCP_REL}: {cmd} serve --mcp",
                            harness_label(h)
                        )?;
                    }
                    RefreshOutcome::PrintedFallback { hook_file, snippet } => {
                        writeln!(
                            stdout,
                            "  {}: {hook_file} has a foreign or malformed 'doctrine' entry — register manually:",
                            harness_label(h)
                        )?;
                        writeln!(stdout, "{snippet}")?;
                    }
                    RefreshOutcome::None => {}
                }
                // Append-system symlink leg (PHASE-02): pi creates a symlink so
                // the boot snapshot is ingested as append-only context. Claude
                // carries NotApplicable and stays silent.
                match report.append_system {
                    AppendSystemOutcome::CreatedDirAndLink | AppendSystemOutcome::CreatedLink => {
                        writeln!(
                            stdout,
                            "  {tag}{}: created .pi/APPEND_SYSTEM.md symlink",
                            harness_label(h)
                        )?;
                    }
                    AppendSystemOutcome::ReplacedLink => {
                        writeln!(
                            stdout,
                            "  {tag}{}: replaced .pi/APPEND_SYSTEM.md symlink",
                            harness_label(h)
                        )?;
                    }
                    AppendSystemOutcome::NoOp | AppendSystemOutcome::NotApplicable => {}
                }
                // Pi extension leg (PHASE-03): pi generates/refreshes a
                // session_start extension that runs `doctrine boot`. Claude
                // carries NotApplicable and stays silent.
                match report.extension {
                    ExtOutcome::Generated => {
                        writeln!(
                            stdout,
                            "  {tag}{}: generated extension .pi/extensions/doctrine/index.ts",
                            harness_label(h)
                        )?;
                    }
                    ExtOutcome::Regenerated => {
                        writeln!(
                            stdout,
                            "  {tag}{}: regenerated extension .pi/extensions/doctrine/index.ts",
                            harness_label(h)
                        )?;
                    }
                    ExtOutcome::NoOp | ExtOutcome::NotApplicable => {}
                    ExtOutcome::SkippedForeign => {
                        writeln!(
                            stdout,
                            "  {}: skipped foreign file .pi/extensions/doctrine/index.ts",
                            harness_label(h)
                        )?;
                    }
                }
                // MCP extension leg (SL-120): pi generates/refreshes the MCP
                // bridge extension. Claude carries NotApplicable and stays silent.
                match report.mcp_extension {
                    ExtOutcome::Generated => {
                        writeln!(
                            stdout,
                            "  {tag}{}: generated extension .pi/extensions/doctrine/mcp.ts",
                            harness_label(h)
                        )?;
                    }
                    ExtOutcome::Regenerated => {
                        writeln!(
                            stdout,
                            "  {tag}{}: regenerated extension .pi/extensions/doctrine/mcp.ts",
                            harness_label(h)
                        )?;
                    }
                    ExtOutcome::NoOp | ExtOutcome::NotApplicable => {}
                    ExtOutcome::SkippedForeign => {
                        writeln!(
                            stdout,
                            "  {}: skipped foreign file .pi/extensions/doctrine/mcp.ts",
                            harness_label(h)
                        )?;
                    }
                }
            }
            Err(e) => writeln!(stdout, "  {}: refresh failed: {e:#}", harness_label(h))?,
        }
    }
    Ok(())
}

// ── CLI dispatch ───────────────────────────────────────────────────────────

use clap::Subcommand;

#[derive(Subcommand)]
pub(crate) enum BootCommand {
    /// Wire the `@`-import into CLAUDE.md/AGENTS.md and refresh each harness's
    /// session hook.
    Install {
        /// Explicit project root (default: auto-detect).
        #[arg(short = 'p', long)]
        path: Option<PathBuf>,

        /// Target harness(es): claude, codex. Repeatable. Default: auto-detect.
        #[arg(long = "agent")]
        agent: Vec<String>,

        /// Compute and report the plan without writing anything.
        #[arg(long)]
        dry_run: bool,

        /// Skip the confirmation prompt.
        #[arg(short = 'y', long)]
        yes: bool,
    },
}

pub(crate) fn dispatch(
    command: Option<BootCommand>,
    check: bool,
    emit: bool,
    path: Option<PathBuf>,
    _color: bool,
    command_map: fn() -> String,
) -> anyhow::Result<()> {
    match command {
        None if emit => run_emit(path, command_map),
        None if check => run_check(path, command_map),
        None => run(path, command_map),
        Some(BootCommand::Install {
            path: install_path,
            agent,
            dry_run,
            yes,
        }) => run_install(install_path, &agent, dry_run, yes),
    }
}

