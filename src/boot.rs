/// Pure decision for `.pi/APPEND_SYSTEM.md` symlink state.
/// Reads only Path existence/metadata — no mutation.
fn plan_append_system(root: &Path) -> SymlinkAction {
    let pi_dir = root.join(".pi");
    let link_path = pi_dir.join("APPEND_SYSTEM.md");
    let target = "../.doctrine/state/boot.md";

    if !pi_dir.exists() {
        return SymlinkAction::CreateDirAndLink;
    }
    // Use symlink_metadata, not Path::exists(): the symlink target may
    // not exist yet, but the symlink entry itself is what matters.
    if std::fs::symlink_metadata(&link_path).is_err() {
        return SymlinkAction::CreateLink;
    }
    // Check if it's a symlink pointing to the correct target
    if let Ok(existing_target) = std::fs::read_link(&link_path)
        && existing_target == Path::new(target)
    {
        return SymlinkAction::NoOp;
    }
    SymlinkAction::ReplaceLink
}

/// Imperative apply for the append-system symlink.
fn install_append_system(root: &Path, dry_run: bool) -> anyhow::Result<AppendSystemOutcome> {
    let action = plan_append_system(root);
    let pi_dir = root.join(".pi");
    let link_path = pi_dir.join("APPEND_SYSTEM.md");
    let target = "../.doctrine/state/boot.md";

    let outcome = match action {
        SymlinkAction::CreateDirAndLink => {
            if !dry_run {
                std::fs::create_dir_all(&pi_dir)
                    .with_context(|| format!("Failed to create {}", pi_dir.display()))?;
                unix_fs::symlink(target, &link_path)
                    .with_context(|| format!("Failed to symlink {}", link_path.display()))?;
            }
            AppendSystemOutcome::CreatedDirAndLink
        }
        SymlinkAction::CreateLink => {
            if !dry_run {
                unix_fs::symlink(target, &link_path)
                    .with_context(|| format!("Failed to symlink {}", link_path.display()))?;
            }
            AppendSystemOutcome::CreatedLink
        }
        SymlinkAction::ReplaceLink => {
            if !dry_run {
                std::fs::remove_file(&link_path)
                    .with_context(|| format!("Failed to remove {}", link_path.display()))?;
                unix_fs::symlink(target, &link_path)
                    .with_context(|| format!("Failed to symlink {}", link_path.display()))?;
            }
            AppendSystemOutcome::ReplacedLink
        }
        SymlinkAction::NoOp => AppendSystemOutcome::NoOp,
    };
    Ok(outcome)
}

