/// The snapshot lives in the runtime-state tree — derived, gitignored
/// (inherits the `.doctrine/*` ignore), `rm -rf`-able. Never authoritative.
const BOOT_REL: &str = ".doctrine/state/boot.md";

/// First line of the snapshot — warns a human off editing a regenerated file.
const BOOT_HEADER: &str =
    "<!-- Generated by `doctrine boot` — do not edit; regenerated each session -->";

/// Sentinel injected into the boot snapshot so an agent can cheaply detect
/// whether the governance context is already in-scope without reading disk.
const BOOT_SENTINEL: &str = "BOOT-SENTINEL: doctrine-governance-snapshot";

/// Ownership marker for `.pi/extensions/doctrine/index.ts` — first line of
/// each generated file, so we can distinguish doctrine-authored extensions
/// from hand-authored ones without parsing TypeScript.
const PI_EXT_HEADER: &str = "// Generated by `doctrine boot install` — do not edit.";

/// The project's user-owned governance pointer layer — seeded by `doctrine
/// install` (Skip-if-exists), read here as the `Governance` section body, or a
/// benign marker when absent. NOT the embed: this file is the editable layer.
const GOVERNANCE_REL: &str = ".doctrine/governance.md";

/// Optional user-authored onboarding footer injected after the Memory section,
/// before the build-volatile Invoking doctrine line. Absent → renders a benign
/// marker. The user controls the full body — use it to tell agents to read
/// specific memories or any session-start instructions.
const FOOTER_REL: &str = ".doctrine/boot-footer.md";

// ---------------------------------------------------------------------------
// Pure: snapshot assembly (the extensible seam)
// ---------------------------------------------------------------------------

/// One rendered section: a heading and its already-produced body.
struct Section {
    heading: String,
    body: String,
}

/// Where a section's body comes from. New governance kinds append a `GovRows`
/// row to the sequence with no other change. `ExecPath` is build-volatile, not
/// governance, so it is ordered **last** (see `boot_sequence`).
enum SourceKind {
    /// The resolved `current_exe()` path — so the agent invokes the off-PATH CLI.
    ExecPath,
    /// An embedded digest asset, named by its `install/`-relative filename. The
    /// name selects the canonical embed read by `produce(Static)` via
    /// `install::asset_text` — deterministic and not user-clobberable.
    Static(&'static str),
    /// The project's `.doctrine/governance.md` body, read from disk (the
    /// user-owned layer), or a marker when absent.
    Governance,
    /// A governance-kind listing filtered to its in-force status SET — accepted
    /// ADRs, required policies, default+required standards. One arm projects all
    /// numbered governance kinds: the kind descriptor + the status set that
    /// reveals past the list hide-set (SL-033 — the per-kind-arm collapse).
    GovRows(&'static governance::GovKind, &'static [&'static str]),
    /// Memory pointers — its own variant: `memory::list_rows` takes a distinct
    /// signature (a `None` scope arg), not the `GovKind` spine.
    Memories,
    /// The dense command-map projection (SL-150 §5.4). A marker variant — no
    /// payload, so `SourceKind` stays a clean data enum (its derives, incl.
    /// `Debug`, still hold). The renderer is INJECTED into `produce`/
    /// `build_sections` (a `fn() -> String` param), never named here: `boot`
    /// must not depend on `cli` (D9 — the layering edge stays `cli → boot`).
    CommandMap,
    /// Optional `.doctrine/boot-footer.md` body, read from disk.
    Footer,
}

/// The declared heading + source order. The `Invoking doctrine` (`ExecPath`)
/// section is **last**: the exec path is build-volatile (§5.3/F4), so ordering
/// it last confines a path change to the snapshot tail and leaves the
/// governance prefix cache-warm.
fn boot_sequence() -> Vec<(&'static str, SourceKind)> {
    vec![
        (
            "Routing & Process",
            SourceKind::Static("routing-process.md"),
        ),
        // The dense command-map projection — placed immediately after routing so
        // the agent meets the command surface before governance (SL-150).
        ("Commands", SourceKind::CommandMap),
        ("Governance (project)", SourceKind::Governance),
        (
            "Accepted ADRs",
            SourceKind::GovRows(&adr::ADR_KIND, &["accepted"]),
        ),
        (
            "Active Policies",
            SourceKind::GovRows(&policy::POLICY_KIND, &["required"]),
        ),
        (
            "Active Standards",
            SourceKind::GovRows(&standard::STANDARD_KIND, &["default", "required"]),
        ),
        ("Memory", SourceKind::Memories),
        // user-authored footer — optional project instructions (e.g. required
        // memory retrieval). Renders marker when absent.
        ("Onboarding", SourceKind::Footer),
        // build-volatile — keep LAST so a path change hits only the cache tail.
        ("Invoking doctrine", SourceKind::ExecPath),
    ]
}

/// Assemble the snapshot string: header comment, title, then each section as
/// `## {heading}\n{body}`. Deterministic — same sections in, byte-identical out.
fn render_boot(sections: &[Section]) -> String {
    let mut out = String::new();
    out.push_str(BOOT_HEADER);
    out.push('\n');
    out.push_str("# Doctrine Boot Context\n");
    out.push_str("<!-- ");
    out.push_str(BOOT_SENTINEL);
    out.push_str(" -->\n");
    for section in sections {
        out.push_str("\n## ");
        out.push_str(&section.heading);
        out.push('\n');
        out.push_str(&section.body);
        out.push('\n');
    }
    out
}

/// The fixed body for a not-yet-populated section. Fixed (no clock/inputs) so
/// `render_boot` stays byte-stable and the content-diff cache key holds.
fn marker(label: &str) -> String {
    format!("<!-- {label}: not yet populated -->")
}

/// A governance-specific nudge for empty Policies/Standards sections — points
/// at the shipped signpost memory covering these governance kinds.
fn gov_nudge(heading: &str) -> String {
    let kind = heading
        .strip_prefix("Active ")
        .unwrap_or(heading)
        .to_lowercase();
    format!("<!-- No active {kind} yet. See mem.signpost.doctrine.policies-standards -->")
}

/// Strip HTML comments (`<!-- ... -->`) from `s`, collapsing runs of
/// 3+ consecutive newlines to at most 2 (eliminates blank-line artifacts
/// from removed comments). Leading/trailing whitespace is trimmed (the
/// result is injected into a code-block section heading, not free-form
/// prose, so leading indentation is not semantic).
///
/// Single pass — no intermediate allocation beyond the output buffer.
fn strip_html_comments(s: &str) -> String {
    let mut out = String::with_capacity(s.len());
    let mut rest = s;
    let mut newline_count = 0u8;

    while let Some(start) = rest.find("<!--") {
        // Push content before comment, collapsing newlines.
        for c in rest[..start].chars() {
            push_collapsed_newline(&mut out, c, &mut newline_count);
        }
        if let Some(end) = rest[start..].find("-->") {
            rest = &rest[start + end + 3..];
        } else {
            rest = "";
            break;
        }
    }
    // Push remaining content after last comment.
    for c in rest.chars() {
        push_collapsed_newline(&mut out, c, &mut newline_count);
    }

    out.trim().to_string()
}

/// Push `c` to `out`, capping consecutive newlines at 2.
/// A non-newline character resets the counter.
fn push_collapsed_newline(out: &mut String, c: char, count: &mut u8) {
    if c == '\n' {
        *count = count.saturating_add(1);
        if *count <= 2 {
            out.push(c);
        }
    } else {
        *count = 0;
        out.push(c);
    }
}

